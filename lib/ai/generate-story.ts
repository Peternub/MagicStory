import "server-only";
import type { StoryInput } from "@/lib/validators/stories";

type ChildProfile = {
  name: string;
  age: number;
  gender: "boy" | "girl";
  interests?: string | null;
  fears?: string | null;
  additional_context?: string | null;
};

type GenerateStoryParams = {
  child: ChildProfile;
  request: StoryInput;
  childIsProtagonist?: boolean;
};

type GeneratedStory = {
  title: string;
  text: string;
  provider: string;
};

type OpenAiResponse = {
  choices?: Array<{
    message?: {
      content?: string;
    };
  }>;
};

function getTargetLength(minutes: number) {
  switch (minutes) {
    case 3:
      return "450-650 слов";
    case 5:
      return "700-950 слов";
    case 7:
      return "950-1250 слов";
    case 10:
      return "1300-1650 слов";
    default:
      return "700-950 слов";
  }
}

function getGenderLabel(gender: ChildProfile["gender"]) {
  return gender === "girl" ? "девочка" : "мальчик";
}

function getChildPronoun(gender: ChildProfile["gender"]) {
  return gender === "girl" ? "она" : "он";
}

function getGenderedWords(gender: ChildProfile["gender"]) {
  if (gender === "girl") {
    return {
      sat: "села",
      said: "сказала",
      looked: "посмотрела",
      fixed: "поправила",
      noticed: "заметила",
      chose: "выбрала",
      settled: "устроилась",
      understood: "поняла",
      managed: "справилась"
    };
  }

  return {
    sat: "сел",
    said: "сказал",
    looked: "посмотрел",
    fixed: "поправил",
    noticed: "заметил",
    chose: "выбрал",
    settled: "устроился",
    understood: "понял",
    managed: "справился"
  };
}

